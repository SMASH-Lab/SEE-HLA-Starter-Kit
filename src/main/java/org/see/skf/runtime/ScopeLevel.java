package org.see.skf.runtime;

/**
 * Object class attributes can have different scope levels: Publish-only, Subscribe-only, Publish-Subscribe, and None.
 * The framework honors these scope levels when carrying out publish-subscribe operations for an object class.
 * Scope levels enable the optimization of the amount of object class data flowing into the federate.
 *
 * @since 2.0
 */
public enum ScopeLevel {
    PUBLISH,
    SUBSCRIBE,
    PUBLISH_SUBSCRIBE,
    NONE
}
